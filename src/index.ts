/**
 * @description
 * @param {number} n
 * @returns {boolean}
 * @example
 * isOdd(1) // true
 * isOdd(2) // false
 * */
export const isOdd = (n: number) => n % 2 === 1;

/**
 * @description
 * @param {number} n
 * @returns {boolean}
 * @example
 * isEven(1) // false
 * isEven(2) // true
 * */
export const isEven = (n: number) => n % 2 === 0;
